// Get current language from localStorage or default to English
function getCurrentLanguage() {
  return localStorage.getItem("language") || "en";
}

// Set language and save to localStorage
function setLanguage(lang) {
  localStorage.setItem("language", lang);
}

// Translate text
function t(key) {
  const lang = getCurrentLanguage();
  return translations[lang][key] || translations["en"][key] || key;
}

// Update all UI elements on page
function updateUILanguage() {
  const lang = getCurrentLanguage();
  
  // Update all elements with data-i18n attribute
  document.querySelectorAll("[data-i18n]").forEach(el => {
    const key = el.getAttribute("data-i18n");
    if ((el.tagName === "INPUT" && el.type === "text") || el.tagName === "TEXTAREA") {
      el.placeholder = t(key);
    } else {
      el.textContent = t(key);
    }
  });
  
  // Update language button text
  const langBtn = document.getElementById("langToggleBtn");
  if (langBtn) {
    langBtn.textContent = lang === "en" ? "हिंदी" : "English";
    langBtn.title = lang === "en" ? "Switch to Hindi" : "Switch to English";
  }
}

// Toggle between languages
async function toggleLanguage() {
  const currentLang = getCurrentLanguage();
  const newLang = currentLang === "en" ? "hi" : "en";
  setLanguage(newLang);
  updateUILanguage();
  
  // Reload page data if needed
  if (window.location.pathname.includes("alerts.html")) {
    await loadAlerts();
  } else if (window.location.pathname.includes("index.html") || window.location.pathname === "/") {
    await loadDashboard();
  } else if (window.location.pathname.includes("json.html")) {
    loadJSONView();
  } else if (window.location.pathname.includes("weather.html") && typeof loadWeatherPage === "function") {
    await loadWeatherPage();
  }

  // Refresh notification translations too
  if (document.getElementById("notificationList")) {
    await loadNotifications();
  }

  // Re-render currently shown analysis result in the newly selected language.
  if (typeof window.refreshAnalysisResultForLanguage === "function") {
    await window.refreshAnalysisResultForLanguage();
  }
}

// Initialize language on page load
window.addEventListener("DOMContentLoaded", () => {
  updateUILanguage();
});
